/*--- FTMatクラス -----------------------------------------------------
	Matクラスの画像を簡単にフーリエ変換するための画像クラス
	ImgMat <=(1次元化)=> ImgVec <=(DFT)=> dft_ImgVec
	ZeroPoint:		インデックスのゼロ点 (0:左上, 1:中心)
	IndexOrder:		インデックス順序 (0:ラスタスキャン, 1:逆ラスタスキャン)
	PaddingWay:		パディング手法 (0:BORDER_CONSTANT, 1:BORDER_REPLICATE)
	Cols, Rows, Size:		DFT前のサイズ
	Xsize, Ysize, XYsize:	DFT後のサイズ
-----------------------------------------------------------------------*/
class FTMat {
private:
	int x = 0, y = 0;
	int zeroX = 0, zeroY = 0, zeroIndex = 0;
public:
	Mat ImgMat;		// 2次元画像
	Mat ImgVec;		// 1次元化画像(Mat)
	Mat dft_ImgVec;	// 1次元化画像のフーリエ変換
	int ZeroPoint = 0;		// 1次元化の際のインデックスのゼロ点
	int IndexOrder = 0;		// 1次元化の際のインデックス順序
	int PaddingWay = 0;		// パディング手法
	int Half_width = 0;		// パディングの増加分(増加画像の幅/2)
	int Half_height = 0;	// パディングの増加分(増加画像の高さ/2)
	int Cols = 0;		// 画像の幅
	int Rows = 0;		// 画像の高さ
	int Sizes = 0;		// 画像の総ピクセル数
	int Xsize = 0;		// DFT後の画像の幅
	int Ysize = 0;		// DFT後の画像の高さ
	int XYsize = 0;		// DFT後の画像の総ピクセル数

	FTMat();										// 初期化
	FTMat(Mat& Input_ImgMat, int Input_checker);	// 入力指定による初期化(0:ImgMat,1:ImgVec,2:dft_ImgVec)

	void toMatrix(int Zero, int Order, int SizeX, int SizeY, int DFTsizeX, int DFTsizeY);	// 画像の2次元化
	void toVector(int Zero, int Order, int Padding, int DFTsizeX, int DFTsizeY);			// 画像の1次元化
	void DFT();		// forward DFT
	void iDFT();	// inverse DFT
	void setting(int Zero, int Order, int SizeX, int SizeY, int Padding, int DFTsizeX, int DFTsizeY);

	void copyTo(FTMat& Output_FTMat);							// FTMatクラスのコピー
	void output(Mat& Output_Img_gray);							// 2次元画像の出力
	void resizeTo(FTMat& Output_FTMat, double ResizeFactor);	// 2次元画像のリサイズ
};
FTMat::FTMat() {
	x = 0, y = 0;
	zeroX = 0, zeroY = 0, zeroIndex = 0;
	ImgMat.release();
	ImgVec.release();
	dft_ImgVec.release();
	ZeroPoint = 0;
	IndexOrder = 0;
	PaddingWay = 0;
	Half_width = 0;
	Half_height = 0;
	Cols = 0;
	Rows = 0;
	Sizes = 0;
	Xsize = 0;
	Ysize = 0;
	XYsize = 0;
}
FTMat::FTMat(Mat& Input_ImgMat, int Input_checker) {
	if (Input_checker == 0) {
		if (Input_ImgMat.channels() != 1) { cout << "WARNING! FTMat() : Input image is not 1 channel. It's " << Input_ImgMat.channels() << " channels." << endl; }
		Input_ImgMat.convertTo(ImgMat, CV_64FC1);
		Cols = ImgMat.cols;
		Rows = ImgMat.rows;
		Sizes = Cols * Rows;
		ImgVec.release();
		dft_ImgVec.release();
		ZeroPoint = 0;
		IndexOrder = 0;
		PaddingWay = 0;
		Xsize = 0;
		Ysize = 0;
		XYsize = 0;
	}
	else if (Input_checker == 1) {
		if (Input_ImgMat.channels() != 1) { cout << "WARNING! FTMat() : Input image is not 1 channel. It's " << Input_ImgMat.channels() << " channels." << endl; }
		Input_ImgMat.convertTo(ImgVec, CV_64FC1);
		Xsize = ImgVec.cols;
		Ysize = ImgVec.rows;
		XYsize = Xsize * Ysize;
		if (Ysize != 1) { cout << "WARNING! FTMat() : Input image is not 1D vector. The Matrix is (" << Xsize << ", " << Ysize << ")." << endl; }
		ImgMat.release();
		dft_ImgVec.release();
		ZeroPoint = 0;
		IndexOrder = 0;
		PaddingWay = 0;
		Cols = 0;
		Rows = 0;
		Sizes = 0;
	}
	else if (Input_checker == 2) {
		if (Input_ImgMat.channels() != 2) { cout << "WARNING! FTMat() : Input image is not 2 channel. It's " << Input_ImgMat.channels() << " channels." << endl; }
		Input_ImgMat.convertTo(dft_ImgVec, CV_64FC2);
		Xsize = dft_ImgVec.cols;
		Ysize = dft_ImgVec.rows;
		XYsize = Xsize * Ysize;
		ImgMat.release();
		ImgVec.release();
		ZeroPoint = 0;
		IndexOrder = 0;
		PaddingWay = 0;
		Cols = 0;
		Rows = 0;
		Sizes = 0;
	}
	else { cout << "ERROR! FTMat() : Input_checker is wrong." << endl; }
}
void FTMat::toMatrix(int Zero, int Order, int SizeX, int SizeY, int DFTsizeX, int DFTsizeY) {
	ZeroPoint = Zero;
	IndexOrder = Order;
	Cols = SizeX;
	Rows = SizeY;
	Sizes = Cols * Rows;
	Xsize = DFTsizeX;
	Ysize = DFTsizeY;
	XYsize = Xsize * Ysize;

	// インデックスを指定して2次元ベクトルに変換
	Mat ImgMat_sub = Mat::zeros(Ysize, Xsize, CV_64FC1);
	zeroX = 0, zeroY = 0;	// 左上ゼロ
	if (ZeroPoint == 1) { zeroX = (int)(Xsize / 2), zeroY = (int)(Ysize / 2); }	// 中心ゼロ
	else if (ZeroPoint != 0) { cout << "WARNNING! FTMat.toMatrix() : ZeroPoint is wrong." << endl; }
	zeroIndex = 0;
	if (IndexOrder == 0) {
		for (y = 0; y < Ysize; y++) {
			for (x = 0; x < Xsize; x++) {
				ImgMat_sub.at<double>(zeroY, zeroX) = ImgVec.at<double>(0, zeroIndex);
				zeroX++;
				if (zeroX >= Xsize) {
					zeroX = 0;
					zeroY++;
					if (zeroY >= Ysize) { zeroY = 0; }
				}
				zeroIndex++;
			}
		}
	}
	else if (IndexOrder == 1) {
		for (y = 0; y < Ysize; y++) {
			for (x = 0; x < Xsize; x++) {
				ImgMat_sub.at<double>(zeroY, zeroX) = ImgVec.at<double>(0, zeroIndex);
				zeroX--;
				if (zeroX < 0) {
					zeroX = Xsize - 1;
					zeroY--;
					if (zeroY < 0) { zeroY = Ysize - 1; }
				}
				zeroIndex++;
			}
		}
	}
	else { cout << "ERROR! FTMat.toMatrix() : IndexOrder is wrong." << endl; }

	// 最適な画像の切り出し
	Half_width = (int)((Xsize - Cols) / 2);
	Half_height = (int)((Ysize - Rows) / 2);
	ImgMat = ImgMat_sub(Rect(Half_width, Half_height, Cols, Rows));

	// メモリの解放
	ImgMat_sub.release();
}
void FTMat::toVector(int Zero, int Order, int Padding, int DFTsizeX, int DFTsizeY) {
	ZeroPoint = Zero;
	IndexOrder = Order;
	PaddingWay = Padding;
	Xsize = DFTsizeX;
	Ysize = DFTsizeY;
	XYsize = Xsize * Ysize;

	// DFT変換のパディング幅を求める(縦横各拡大幅の半分)
	Half_width = (int)((Xsize - ImgMat.cols) / 2);
	Half_height = (int)((Ysize - ImgMat.rows) / 2);
	// DFT変換のパディング
	Mat Padding_ImgMat = Mat::zeros(Ysize, Xsize, CV_64FC1);
	if (PaddingWay == 0) { copyMakeBorder(ImgMat, Padding_ImgMat, Half_height, Ysize - ImgMat.rows - Half_height, Half_width, Xsize - ImgMat.cols - Half_width, BORDER_CONSTANT, 0.0); }
	else if (PaddingWay == 1) { copyMakeBorder(ImgMat, Padding_ImgMat, Half_height, Ysize - ImgMat.rows - Half_height, Half_width, Xsize - ImgMat.cols - Half_width, BORDER_REPLICATE); }
	else { cout << "ERROR! FTMat.toVector() : PaddingWay is wrong." << endl; }

	// インデックスを指定して1次元ベクトルに変換
	ImgVec = Mat::zeros(1, XYsize, CV_64FC1);
	zeroX = 0, zeroY = 0;	// 左上ゼロ
	if (ZeroPoint == 1) { zeroX = (int)(Xsize / 2), zeroY = (int)(Ysize / 2); }	// 中心ゼロ
	else if (ZeroPoint != 0) { cout << "WARNNING! FTMat.toVector() : ZeroPoint is wrong." << endl; }
	zeroIndex = 0;
	if (IndexOrder == 0) {
		for (y = 0; y < Ysize; y++) {
			for (x = 0; x < Xsize; x++) {
				ImgVec.at<double>(0, zeroIndex) = Padding_ImgMat.at<double>(zeroY, zeroX);
				zeroX++;
				if (zeroX >= Xsize) {
					zeroX = 0;
					zeroY++;
					if (zeroY >= Ysize) { zeroY = 0; }
				}
				zeroIndex++;
			}
		}
	}
	else if (IndexOrder == 1) {
		for (y = 0; y < Ysize; y++) {
			for (x = 0; x < Xsize; x++) {
				ImgVec.at<double>(0, zeroIndex) = Padding_ImgMat.at<double>(zeroY, zeroX);
				zeroX--;
				if (zeroX < 0) {
					zeroX = Xsize - 1;
					zeroY--;
					if (zeroY < 0) { zeroY = Ysize - 1; }
				}
				zeroIndex++;
			}
		}
	}
	else { cout << "ERROR! FTMat.toVector() : IndexOrder is wrong." << endl; }

	// メモリの解放
	Padding_ImgMat.release();
}
void FTMat::DFT() {
	Mat ImgVec_2D;
	// 複素数用に2チャンネルに変更
	Mat planes[] = { Mat_<double>(ImgVec), Mat::zeros(ImgVec.size(), CV_64F) };
	merge(planes, 2, ImgVec_2D);
	// DFT変換
	dft(ImgVec_2D, dft_ImgVec, 0);

	// メモリの解放
	ImgVec_2D.release();
	planes[0].release();
	planes[1].release();
}
void FTMat::iDFT() {
	ImgVec.release();
	// inverseDFT変換
	dft(dft_ImgVec, ImgVec, cv::DFT_INVERSE + cv::DFT_SCALE | cv::DFT_REAL_OUTPUT);
}
void FTMat::setting(int Zero, int Order, int SizeX, int SizeY, int Padding, int DFTsizeX, int DFTsizeY) {
	ZeroPoint = Zero;
	IndexOrder = Order;
	Cols = SizeX;
	Rows = SizeY;
	Sizes = Cols * Rows;
	PaddingWay = Padding;
	Xsize = DFTsizeX;
	Ysize = DFTsizeY;
	XYsize = Xsize * Ysize;
}
void FTMat::copyTo(FTMat& Output_FTMat) {
	Output_FTMat = FTMat();
	ZeroPoint = Output_FTMat.ZeroPoint;
	IndexOrder = Output_FTMat.IndexOrder;
	PaddingWay = Output_FTMat.PaddingWay;
	Cols = Output_FTMat.Cols;
	Rows = Output_FTMat.Rows;
	Sizes = Cols * Rows;
	Xsize = Output_FTMat.Xsize;
	Ysize = Output_FTMat.Ysize;
	XYsize = Xsize * Ysize;
	ImgMat.copyTo(Output_FTMat.ImgMat);
	ImgVec.copyTo(Output_FTMat.ImgVec);
	dft_ImgVec.copyTo(Output_FTMat.dft_ImgVec);
}
void FTMat::output(Mat& Output_Img_gray) {
	// 画像をCV_8Uに変換
	ImgMat.convertTo(Output_Img_gray, CV_8UC1);
}
void FTMat::resizeTo(FTMat& Output_FTMat, double ResizeFactor) {
	Output_FTMat = FTMat();
	Mat Resize_FTMat;
	resize(ImgMat, Resize_FTMat, Size(), ResizeFactor, ResizeFactor);
	Output_FTMat = FTMat(Resize_FTMat, 0);

	// メモリの解放
	Resize_FTMat.release();
}


