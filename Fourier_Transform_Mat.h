void KERNEL::normalization() {
	/* 総和を計算 */
	sum = 0.0;
#pragma omp parallel for private(x)
	for (y = 0; y < ImgMat.rows; y++) {
		for (x = 0; x < ImgMat.cols; x++) {
			sum += (double)ImgMat.at<double>(y, x);
		}
	}

	double multi_sum = 1.0 / (double)sum;		// 総和が１になるような係数
	/* 総和を１にする */
	double calc_sum;
#pragma omp parallel for private(x)
	for (y = 0; y < ImgMat.rows; y++) {
		for (x = 0; x < ImgMat.cols; x++) {
			calc_sum = (double)ImgMat.at<double>(y, x);
			calc_sum *= multi_sum;
			ImgMat.at<double>(y, x) = calc_sum;
		}
	}
	sum = 1.0;
}
