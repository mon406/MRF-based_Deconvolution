/*--- FTMat3Dクラス ---------------------------------------------------
	FTMatクラスでRGB画像扱うための画像クラス
		FT_Mat[0]:	カラー画像のB
		FT_Mat[1]:	カラー画像のG
		FT_Mat[2]:	カラー画像のR
-----------------------------------------------------------------------*/
class FTMat3D {
private:
	int x = 0, y = 0, c = 3;
public:
	vector<FTMat> FT_Mat;						// 2次元RGB画像
	double AverageColor[3] = { 0.0, 0.0, 0.0 };	// RGBの平均値

	FTMat3D();															// 初期化
	FTMat3D(Mat& Input_ImgMat_color);									// 入力指定による初期化(ImgMat)
	FTMat3D(Mat& dft_ImgVec_0, Mat& dft_ImgVec_1, Mat& dft_ImgVec_2);	// 入力指定による初期化(dft_ImgVec)

	void toMatrix(int Zero, int Order, int SizeX, int SizeY, int DFTsizeX, int DFTsizeY);	// 画像の2次元化
	void toVector(int Zero, int Order, int Padding, int DFTsizeX, int DFTsizeY);			// 画像の1次元化
	void DFT();		// forward DFT
	void iDFT();	// inverse DFT
	void setting(int Zero, int Order, int SizeX, int SizeY, int Padding, int DFTsizeX, int DFTsizeY);
	void settingAverageColor(FTMat3D& Input);	// RGBの平均値の設定

	void copyTo(FTMat3D& Output_FTMat);								// FTMat3Dクラスのコピー
	void output(Mat& Output_Img_color);								// 2次元画像の出力
	void resizeTo(FTMat3D& Output_FTMat3D, double ResizeFactor);	// 2次元画像のリサイズ
};
FTMat3D::FTMat3D() {
	x = 0, y = 0, c = 3;
	FT_Mat.clear();
	AverageColor[0] = 0.0;
	AverageColor[1] = 0.0;
	AverageColor[2] = 0.0;
}
FTMat3D::FTMat3D(Mat& Input_ImgMat_color) {
	FT_Mat.clear();
	if (Input_ImgMat_color.channels() != 3) { cout << "WARNING! FTMat3D() : Input image is not 3 channel. It's " << (int)Input_ImgMat_color.channels() << " channels." << endl; }
	else {
		// 画像をCV_64Fに変換 (3つのチャネルB, G, Rに分離)
		Mat Input_doubleImgMat;
		Input_ImgMat_color.convertTo(Input_doubleImgMat, CV_64FC3);
		Mat doubleImgMat[3] = { Mat::zeros(Input_doubleImgMat.size(), CV_64F), Mat::zeros(Input_doubleImgMat.size(), CV_64F), Mat::zeros(Input_doubleImgMat.size(), CV_64F) };
		split(Input_doubleImgMat, doubleImgMat);

		// 画像をFTMatに変換
		for (c = 0; c < 3; c++) {
			// 画像を0-1正規化
			doubleImgMat[c].convertTo(doubleImgMat[c], CV_64F, 1.0 / 255);

			// 画像を中心化
			AverageColor[c] = (double)mean(doubleImgMat[c])[0];
			doubleImgMat[c] -= (double)AverageColor[c];

			FTMat Input_ImgMat = FTMat(doubleImgMat[c], 0);
			FT_Mat.push_back(Input_ImgMat);

			// メモリの解放
			Input_ImgMat = FTMat();
		}
		//cout << " FTMat3D() : - AverageColor = [" << AverageColor[0] << ", " << AverageColor[1] << ", " << AverageColor[2] << "]" << endl;	// 平均値確認用

		// メモリの解放
		Input_doubleImgMat.release();
		doubleImgMat[0].release();
		doubleImgMat[1].release();
		doubleImgMat[2].release();
	}
}
FTMat3D::FTMat3D(Mat& dft_ImgVec_0, Mat& dft_ImgVec_1, Mat& dft_ImgVec_2) {
	FT_Mat.clear();
	if (dft_ImgVec_0.channels() != 2 || dft_ImgVec_1.channels() != 2 || dft_ImgVec_2.channels() != 2) {
		cout << "WARNING! FTMat3D() : Input image is not 2 channel." << endl;
	}
	else {
		Mat dft_doubleImgVec[3];
		dft_ImgVec_0.copyTo(dft_doubleImgVec[0]);
		dft_ImgVec_1.copyTo(dft_doubleImgVec[1]);
		dft_ImgVec_2.copyTo(dft_doubleImgVec[2]);

		// 画像をFTMatに変換
		for (c = 0; c < 3; c++) {
			FTMat Input_ImgMat = FTMat(dft_doubleImgVec[c], 2);
			FT_Mat.push_back(Input_ImgMat);
			// メモリの解放
			Input_ImgMat = FTMat();
		}

		// メモリの解放
		dft_doubleImgVec[0].release();
		dft_doubleImgVec[1].release();
		dft_doubleImgVec[2].release();
	}
}
void FTMat3D::toMatrix(int Zero, int Order, int SizeX, int SizeY, int DFTsizeX, int DFTsizeY) {
	for (c = 0; c < 3; c++) {
		FT_Mat[c].toMatrix(Zero, Order, SizeX, SizeY, DFTsizeX, DFTsizeY);
	}
}
void FTMat3D::toVector(int Zero, int Order, int Padding, int DFTsizeX, int DFTsizeY) {
	for (c = 0; c < 3; c++) {
		FT_Mat[c].toVector(Zero, Order, Padding, DFTsizeX, DFTsizeY);
	}
}
void FTMat3D::DFT() {
	for (c = 0; c < 3; c++) {
		FT_Mat[c].DFT();
	}
}
void FTMat3D::iDFT() {
	for (c = 0; c < 3; c++) {
		FT_Mat[c].iDFT();
	}
}
void FTMat3D::setting(int Zero, int Order, int SizeX, int SizeY, int Padding, int DFTsizeX, int DFTsizeY) {
	for (c = 0; c < 3; c++) {
		FT_Mat[c].setting(Zero, Order, SizeX, SizeY, Padding, DFTsizeX, DFTsizeY);
	}
}
void FTMat3D::settingAverageColor(FTMat3D& Input) {
	for (c = 0; c < 3; c++) {
		AverageColor[c] = (double)Input.AverageColor[c];
	}
}
void FTMat3D::copyTo(FTMat3D& Output_FTMat) {
	Output_FTMat = FTMat3D();
	for (c = 0; c < 3; c++) {
		Output_FTMat.FT_Mat.push_back(FT_Mat[c]);
		Output_FTMat.AverageColor[c] = AverageColor[c];
	}
}
void FTMat3D::output(Mat& Output_Img_color) {
	// 3つのチャネルB, G, Rを統合
	Mat doubleImgMat[3];
	double RGB_checker = 0.0;
	for (c = 0; c < 3; c++) {
		FT_Mat[c].ImgMat.copyTo(doubleImgMat[c]);

		// 画像を中心化復元
		doubleImgMat[c] += (double)AverageColor[c];

		// 画像を0-255化
		doubleImgMat[c].convertTo(doubleImgMat[c], CV_64F, 255);
#pragma omp parallel for private(x)
		for (y = 0; y < doubleImgMat[c].rows; y++) {
			for (x = 0; x < doubleImgMat[c].cols; x++) {
				RGB_checker = (double)doubleImgMat[c].at<double>(y, x);
				if (RGB_checker < 0) { doubleImgMat[c].at<double>(y, x) = 0.0; }
				else if (RGB_checker > MAX_INTENSE) { doubleImgMat[c].at<double>(y, x) = 255.0; }
			}
		}
	}
	//cout << " FTMat3D() : + AverageColor = [" << AverageColor[0] << ", " << AverageColor[1] << ", " << AverageColor[2] << "]" << endl;	// 平均値確認用
	if (AverageColor[0] == 0 && AverageColor[1] == 0 && AverageColor[2] == 0) { cout << "WARNING! FTMat3D() : Set AverageColors." << endl; }
	merge(doubleImgMat, 3, Output_Img_color);

	// 画像をCV_8Uに変換
	Output_Img_color.convertTo(Output_Img_color, CV_8UC3);

	// メモリの解放
	doubleImgMat[0].release();
	doubleImgMat[1].release();
	doubleImgMat[2].release();
}
void FTMat3D::resizeTo(FTMat3D& Output_FTMat3D, double ResizeFactor) {
	for (c = 0; c < 3; c++) {
		FT_Mat[c].resizeTo(Output_FTMat3D.FT_Mat[c], ResizeFactor);
		Output_FTMat3D.AverageColor[c] = AverageColor[c];
	}
}


